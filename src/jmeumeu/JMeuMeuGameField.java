    {
    	this.jLabel4.setText(Integer.toString(_View._Datas.ecus));
    }
    
    private String IsCowExist()
    {
        ResultSet RS = _View._Oracle.Query("SELECT ABREUVAGE_VACHE FROM FERME");
        
        String x = null;
        
        try
        {
            x = RS.getString(1);
        }
        catch(SQLException e)
        {
            JMeuMeuHelpers.ShowErrorMessage(e.getMessage());
            return null;
        }
        
        return x;
    }

