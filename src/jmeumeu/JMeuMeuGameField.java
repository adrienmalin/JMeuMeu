    private int GetEcus()
    {
        ResultSet RS = _View._Oracle.Query("SELECT ECUS FROM FERME");
        
        int ecus = 0;
        
        try
        {
            ecus = RS.getInt(1);
        }
        catch(SQLException e)
        {
            JMeuMeuHelpers.ShowErrorMessage(e.getMessage());
            return 0;
        }
        
        return ecus;
    }
    
