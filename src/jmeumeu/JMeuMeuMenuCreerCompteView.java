private void ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActionPerformed

    String Action = evt.getActionCommand();
    
    if(Action.equals("Annuler"))
    {
        this.dispose();
        JMeuMeuApp.getApplication().show(_View);
    }
    else if(Action.equals("CC"))
    {
        if(CheckValues())
        {
            if(_View._Oracle.Connexion())
            {
                if(_View._Oracle.callProc("Creation_Compte", getID()+"', '"+getUserName()+"','"+getPassword()+"','"+getEmail()))
                {
                	JMeuMeuHelpers.ShowInformationMessage("Compte Cr√©e avec Succ√®s");
                }
            }
        }
    }
}//GEN-LAST:event_ButtonActionPerformed

    private boolean CheckValues()
    {
        String Pass1 = "";
        String Pass2 = "";
        
        Pass1 = JMeuMeuHelpers.ToString(jPasswordField1.getPassword());
        Pass2 = JMeuMeuHelpers.ToString(jPasswordField2.getPassword());
     
        if(!Pass1.equals(Pass2))
        {
            JMeuMeuHelpers.ShowErrorMessage(JMeuMeuMessages.WrongPassWords);
            
            return false;
        }
        
        if(!JMeuMeuHelpers.IsEmailAddress(this.jTextField5.getText()))
        {
            JMeuMeuHelpers.ShowErrorMessage(JMeuMeuMessages.WrongEmailFormat);
            
            return false;
        }
        
        return true;
    }

